-- CREAZIONE VISTA locationdefaultview
IF EXISTS(select * from sysobjects where id = object_id(N'[locationdefaultview]') and OBJECTPROPERTY(id, N'IsView') = 1)
DROP VIEW [locationdefaultview]
GO

CREATE VIEW [locationdefaultview] AS SELECT CASE WHEN location.active = 'S' THEN 'Si' WHEN location.active = 'N' THEN 'No' END AS location_active, location.address AS location_address, location.annotations AS location_annotations, location.cap AS location_cap, location.ct AS location_ct, location.cu AS location_cu, location.description, location.extension AS location_extension, [dbo].geo_city.title AS geo_city_title, location.idcity, location.idlocation, [dbo].geo_nation.title AS geo_nation_title, location.idnation, location.latitude AS location_latitude, location.location AS location_location, location.locationcode AS location_locationcode, location.longitude AS location_longitude, location.lt AS location_lt, location.lu AS location_lu, location.newlocationcode AS location_newlocationcode, location.nlevel AS location_nlevel, locationparent.description AS locationparent_description, location.paridlocation, location.rtf AS location_rtf, location.txt AS location_txt, isnull('Descrizione: ' + location.description + '; ','') as dropdown_title FROM location WITH (NOLOCK)  LEFT OUTER JOIN [dbo].geo_city WITH (NOLOCK) ON location.idcity = [dbo].geo_city.idcity LEFT OUTER JOIN [dbo].geo_nation WITH (NOLOCK) ON location.idnation = [dbo].geo_nation.idnation LEFT OUTER JOIN location AS locationparent WITH (NOLOCK) ON location.paridlocation = locationparent.idlocation WHERE  location.idlocation IS NOT NULL 
GO

